int_a = 12
b = 17
int_c = -15
print(type(int_a))  # Output: <class 'int'>
print(type(b))      # Output: <class 'int'> 
print(type(int_c))  # Output: <class 'int'>
petrol = 112
diesel = 95.53
print(type(petrol))  # Output: <class 'int'>
print(type(diesel))  # Output: <class 'float'>
c= 3 + 4j
print(type(c))  # Output: <class 'complex'>
area = "Ameerpet"
area = "Secunderabad"
print(type(area))  # Output: <class 'str'>
company = '''TCS'''  # Using triple quotes for multi-line string
print(type(company))  # Output: <class 'str'>
l = []  # Empty list
print(type(l))  # Output: <class 'list'>
l = ['Rushindra', 'Watch', 'Shoes','Perfume']  # List with mixed data types
print(type(l))  # Output: <class 'list'>
k = ()  # Empty tuple
print(type(k))  # Output: <class 'tuple'>
k= (1, 2.3, "Rushi", "Karimnagar")  # Tuple with mixed data types
print(type(k))  # Output: <class 'tuple'>
d = {}  # Empty dictionary
print(type(d))  # Output: <class 'dict'>
d = {'Name': 'Rushi', 'age': 21, 'Profession': 'Job Seeker'}  # Dictionary with key-value pairs
print(type(d))  # Output: <class 'dict'>
set_a = set()  # Empty set
print(type(set_a))  # Output: <class 'set'>
set_a = {'Rushi', 21, 'Job Seeker', 'Hyderabad'}  # Set with mixed data types
print(type(set_a))  # Output: <class 'set'>
Job = False  # Boolean value
print(type(Job))  # Output: <class 'bool'>